<h2>[MM_ChooseMembership]</h2>

<a onclick="stl_js.insertContent('[MM_ChooseMembership memberTypeId=\'0\']');" class="button-secondary">Insert Tag</a>

<h3>Description:</h3>
<p>This tag outputs a special URL that when clicked, the member type associated with the ID passed is automatically selected and the guest is redirected to the second step of the registration process.</p>

<h3>Attributes:</h3>
<p><span class="mm-code">memberTypeId</span> - The ID associated with member type to automatically select.</p>

<h3>Usage:</h3>

<p><span class="mm-code">[MM_ChooseMembership memberTypeId="2"]</span></p>
