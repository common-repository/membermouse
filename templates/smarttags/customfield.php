<h2>[MM_CustomField]</h2>

<a onclick="stl_js.insertContent('[MM_CustomField name=\'field_name\']');" class="button-secondary">Insert Tag</a>

<h3>Description:</h3>
<p>This tag outputs the value of a custom field provided in the name attribute. </p>

<h3>Attributes:</h3>
<p><span class="mm-code">name</span> - The field name associated with the custom field.</p>

<h3>Usage:</h3>
<p><span class="mm-code">[MM_CustomField name="partner_id"]</span></p>