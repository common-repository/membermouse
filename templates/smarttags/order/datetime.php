<h3>Description:</h3>
<p>This tag outputs the date and time associated with an order. It can only be used in the context of a Confirmation page associated with a product that was purchased. Possible applications for this and other order data access tags are to display data on the screen or send as parameters to an external script to update a 3rd party database.</p>

