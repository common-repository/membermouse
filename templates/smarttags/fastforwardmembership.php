<h2>[MM_FastForwardMembership]</h2>

<a onclick="stl_js.insertContent('[MM_FastForwardMembership days=\'0\' price=\'0\']');" class="button-secondary">Insert Tag</a>

<h3>Description:</h3>
<p>This tag allows the admin to create a way for the member to fast forward their content delivery schedule by paying extra.</p>

<h3>Attributes:</h3>
<p><span class="mm-code">days</span> - This will be the number of days to fast forward their schedule</p>
<p><span class="mm-code">price</span> - This will be the price to charge the member to fast forward their schedule</p>

<h3>Usage:</h3>
<p><span class="mm-code">&lt;a onclick="[MM_FastForwardMembership days="2" price="10.00"]" &gt;Fast Forward&lt;/&gt;</span></p>
