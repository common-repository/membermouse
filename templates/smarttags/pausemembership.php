<h2>[MM_PauseMembership]</h2>

<a onclick="stl_js.insertContent('[MM_PauseMembership redirectUrl=\'http://\']');" class="button-secondary">Insert Tag</a>

<h3>Description:</h3>
<p>This tag outputs a special URL members can visit to pause their membership. When the link is clicked, the member's membership will be paused and they'll be redirected to the URL specified in the <span class="mm-code">redirectUrl</span> attribute.</p>

<h3>Attributes:</h3>
<p><span class="mm-code">redirectUrl</span> - The URL of the page to redirect to after cancellation. This attribute expects an absolute URL.</p>

<h3>Usage:</h3>
<p><span class="mm-code">[MM_PauseMembership redirectUrl="http://www.mydomain.com/specialoffer.php"]</span></p>