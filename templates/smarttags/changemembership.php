<h2>[MM_ChangeMembership]</h2>

<a onclick="stl_js.insertContent('[MM_ChangeMembership memberTypeId=\'0\']');" class="button-secondary">Insert Tag</a>

<h3>Description:</h3>
<p>This tag outputs a special URL that when clicked will change a member's member type. When the link is clicked, the member will be taken to a checkout process where they will confirm their desire to switch to the member type associated with the ID passed.</p>

<h3>Attributes:</h3>
<p><span class="mm-code">memberTypeId</span> - The ID associated with member type to change to.</p>

<h3>Usage:</h3>
<p><span class="mm-code">[MM_ChangeMembership memberTypeId="2"]</span></p>
